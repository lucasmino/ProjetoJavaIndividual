         lblValorMinimo1.setText(leitura2.valorMinimo.toString());
         lblValorMaximo1.setText(leitura2.valorMaximo.toString());
         
         lblValormedio1.setText(String.format("%.2f",leitura2.media/contador));
         //====================================================================//
         leitura3.sortearValor();
         
         pgbTerceira.setStringPainted(true);
         pgbTerceira.setString(leitura3.valorAleatorio.toString());
         pgbTerceira.setValue(leitura3.valorAleatorio);
         
         lblValorMinimo2.setText(leitura3.valorMinimo.toString());
         lblValorMaximo2.setText(leitura3.valorMaximo.toString());
         
