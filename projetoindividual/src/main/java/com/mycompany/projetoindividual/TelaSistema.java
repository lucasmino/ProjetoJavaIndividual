        contador++;
        
         pgbPrimeira.setStringPainted(true);
         pgbPrimeira.setString(leitura.valorAleatorio.toString());
         pgbPrimeira.setValue(leitura.valorAleatorio);
         
         lblValorMinimo.setText(leitura.valorMinimo.toString());
